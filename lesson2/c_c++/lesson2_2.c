#include <stdio.h>

int main ()
{
    /* Арифметические операции в Си и Си++ могут быть сокращенными:
     * полная запись 
     * a = a + 1
     * a = a - 1
     * a = a * 1
     * a = a / 1
     * a = a % 1
     *
     * сокращенная запись
     * a += 1
     * a -= 1
     * a *= 1
     * a /= 1
     * a %= 1 
     *
     * Если в Си и Си++ делим целое число на целое число мы получаем целое
     * число. А если делим целое число на вещественное число, мы получаем
     * вещественное число.
     */

    int a, b;
    a = 3;
    b = 4;
    float x;

    x = 3 / 4;
    printf("x=3/4 => %.f\n", x); // результат: 0
    x = 3./ 4;
    printf("x=3./4 => %.2f\n", x); // результат: 0.75
    x = 3 / 4.;
    printf("x=3/4. => %.2f\n", x); // результат: 0.75
    x = a / 4;
    printf("x=a/4 => %.f\n", x); // результат: 0
    x = a / 4.;
    printf("x=a/4. => %.2f\n", x); // результат: 0.75
    x = 3 / b;
    printf("x=3/b => %.f\n", x); // результат: 0
    x = 3. / b;
    printf("x=3./b => %.2f\n", x); // результат: 0.75
    x = a / b;
    printf("x=a/b => %.f\n", x); // результат: 0
    x = (float)a / b;
    printf("x=(float)a/b => %.2f\n", x); // результат: 0
    x = a / (float)b;
    printf("x=a/(float)b => %.2f\n", x); // результат: 0;

    // В Си если делишь целое число на целое число и кладем ее в переменой с
    // типом вещественное число, на выход получаем вещественное число.
    
    int c, d;
    c = 6;
    d = 3;
    float f;
    
    f = c / d;

    printf("%f\n", f); // result: 2.000000
    printf("%.2f\n", f); // result: 2.00
    printf("%.f\n", f); // result: 2

    // В Си, если перед форматом не точка результатом буде первым из выше
    // написанного примера 2.000000, можно вывести как бы целое число, если формат
    // будет содержать точку (%.f) результатом будет последним из выше
    // написанного примера 2. Даже интересно становится. Дерзаем дальше!!!!

    return 0;
}
