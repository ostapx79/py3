#include <iostream>
#include <cmath>

int main()
{
    using namespace std;

    float x;
    x = floor(-1.6);
    cout << "x=" << x << endl;
    x = ceil(-1.6);
    cout << "x=" << x << endl;
    x = floor(1.6);
    cout << "x=" << x << endl;
    x = ceil(1.6);
    cout << "x=" << x << endl;

    return 0;
}
