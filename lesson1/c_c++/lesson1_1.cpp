#include <iostream>
using namespace std;

int main()
{
    // Variables, and what type they are.
    // Переменные, и какова типа они.
    int i = 2; // integer, целое число
    float f = 23.973; // floating point number, число с плавающей запетой
    char c = 'f'; // one character, один символ

    // console output, вывод на консоль
    cout << i << endl;
    cout << f << endl;
    cout << c << endl;

    return 0;
}
