#include <iostream>
using namespace std;

int main()
{
    int a = 5, b = 3;

    cout << b << "+" << a << "=?" << endl; // 3+5=?
    cout << "Z(" << a << ")=F(" << b << ")" << endl; // Z(5)=F(3)
    cout << "a=" << a << "; b=" << b << ";" << endl; // a=5; b=3;
    cout << "Ответ: (" << a << ";" << b << ")" << endl; // Ответ: (5;3)
    return 0;
}
