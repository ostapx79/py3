#!/usr/bin/python3

first_numb = int(input("Введите любое первое целое число => "))
second_numb = int(input("Введите любое второе целое число => "))

if first_numb > second_numb:
    max_numb = first_numb
else:
    max_numb = second_numb

print("Максимальное целое число из двух введеных =>", max_numb)

